CREATE TABLE uploaded_files (
    id UUID PRIMARY KEY,
    file_name VARCHAR(255) NOT NULL,
    file_size BIGINT NOT NULL,
    uploaded_at TIMESTAMP,
    status VARCHAR(50),
    line_count INTEGER
);
